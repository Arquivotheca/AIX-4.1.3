unsigned short	_toJIS83(unsigned short in_code)
{
	int	i;
	int	low, high;

	low = 0;
	high = sizeof (SJIStoCP932_from) / sizeof (SJIStoCP932_from[0]);
	while (low <= high) {
		i = low + high >> 1;
		if (in_code < SJIStoCP932_from[i])
			high = i - 1;
		else if (in_code > SJIStoCP932_from[i])
			low = i + 1;
		else
			return SJIStoCP932_to[i];
	}
	return 0;
}
